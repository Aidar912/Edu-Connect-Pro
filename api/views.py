from rest_framework import status
from rest_framework.response import Response
from rest_framework.views import APIView
from main.models import Student
from .serializers import StudentSerializer, AccountSerializer, PaymentHistorySerializer, TranscriptSerializer, \
    SubjectSerializer, GradeSerializer, AttendanceSerializer, ScheduleSerializer, TeacherSerializer, GroupSerializer


# Create your views here.



class StudentCreateView(APIView):
    def post(self, request):
        serializer = StudentSerializer(data=request.data)
        if serializer.is_valid():
            serializer.save()
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)




